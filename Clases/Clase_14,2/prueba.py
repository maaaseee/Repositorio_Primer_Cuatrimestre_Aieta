def overwrite_csv(path, lista):
    with open(path, 'w', encoding="utf8") as archivo:
        keys = lista[0].keys()
        cadena = ",".join(keys) + "\n"
        cadenon = ""
        cadenon += cadena

        for dato in lista:
            values = list(dato.values())
            cadena = ""
            for i in range(len(values)):
                if (i + 1) == len(values):
                    cadena += f"{values[i]}\n"
                elif i != len(values):
                    cadena += f"{values[i]},"

            cadenon += cadena

        archivo.write(cadenon)

